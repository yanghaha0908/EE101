     <h1>英国海军</h1>
     <br>
    <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp英国皇家海军，简称为皇家海军（英语：Royal Navy，缩写：RN），是英国的海上作战力量。虽然军舰早在中世纪就被英国国王使用，并投入到百年战争期间的第一场重要海战中。但现代皇家海军起源却要追溯到16世纪初。亦是英国最古老的军种。从17世纪中叶开始，又经历18世纪先后与荷兰、法国争夺过海上霸权。</p>
