      <br>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp1815年维也纳会议标志着英国海上霸主地位的确立，直到19世纪末，英国海军在世界上占有绝对优势，在近一个世纪的时间里，没有哪一个国家有力量向其海上霸主地位挑战。
