由于特殊的地理环境，英国君主大都重视海军的建设与发展，这种传统开始于亨利七世。亨利八世继位后，继承了亨利七世重视海军的政策。他创设了后成为国家重要部门的海军局，下辖码头、食品、武备三大职能部门和其他辅助机构。
