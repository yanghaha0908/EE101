    <p>&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp&nbsp美国海军（英语：United States Navy，缩写：USN或U.S. Navy）是美国武装力量的一个重要军种，负责管理所有与海上军事行动有关的事务，海军部设在五角大楼内。
美国海军下设七个舰队，约500,000现役和预备役军人、279艘现役军舰和4,000架飞机，是世界上规模最庞大、吨位最高、装备最先进、总体实力超过其他国家的世界上最强的海军。美国海军由海军和海军陆战队两个独立的军种组成，都属于美国海军部领导。海军部是美国国防部下属三个军种部门之一， 又称为“华盛顿海军大院”，它由三个中央单位组成：海军部长办公室、海军作战部、海军陆战队总部。另外，美国海岸警卫队在战时受海军领导。</p>
